/** Les différentes variantes disponibles pour la présentation du tableau. */
export enum Variant {
    /** Variante par défaut. */
    DEFAULT = 'default',

    /** Présentation "légère", sans fond mais avec séparateurs. */
    LIGHT = 'light',

    /** Présentation minimaliste, sans fond ni séparateurs. */
    MINIMALIST = 'minimalist',
}
